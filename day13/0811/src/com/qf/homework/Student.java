package com.qf.homework;

public class Student {
    int id;
    String name;
    int age;
    char sex;
    int source;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public char getSex() {
        return sex;
    }

    public int getSource() {
        return source;
    }
}
